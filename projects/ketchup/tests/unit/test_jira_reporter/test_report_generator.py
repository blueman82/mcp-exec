"""
Unit tests for the JIRA reporter report generator.

Tests the ReportGenerator class that generates incident reports from archived
channel messages using AI.
"""

from unittest.mock import AsyncMock, MagicMock

import pytest

from ketchup_unified_scheduler.services.jira_reporter.report_generator import ReportGenerator


class TestReportGenerator:
    """Test the ReportGenerator functionality."""

    @pytest.fixture
    def mock_openai_handler(self):
        """Create a mock OpenAI handler."""
        handler = MagicMock()
        handler.execute_prompt = AsyncMock()
        return handler

    @pytest.fixture
    def mock_channel_msg_ops(self):
        """Create a mock channel message operations."""
        ops = MagicMock()
        ops.fetch_channel_messages = AsyncMock()
        return ops

    @pytest.fixture
    def report_generator(self, mock_openai_handler, mock_channel_msg_ops):
        """Create a ReportGenerator instance with mocked dependencies."""
        return ReportGenerator(
            openai_handler=mock_openai_handler, channel_msg_ops=mock_channel_msg_ops
        )

    @pytest.fixture
    def sample_messages(self):
        """Sample Slack messages for testing (already formatted as strings)."""
        return [
            "[2024-01-01 00:00:00 UTC] User U123: We're seeing authentication errors",
            "[2024-01-01 00:01:00 UTC] User U456: Looking into it now",
            "[2024-01-01 00:02:00 UTC] User U123: Found the issue - certificate expired",
        ]

    @pytest.fixture
    def sample_channel_metadata(self):
        """Sample channel metadata for testing."""
        return {
            "channel_id": "C123456",
            "channel_name": "cso_202401010001_adobe_campaign_12345",
            "customer_name": "Adobe Campaign",
            "jira_ticket": "CPGNREQ-12345",
        }

    @pytest.mark.asyncio
    async def test_generate_report_success(
        self,
        report_generator,
        mock_openai_handler,
        mock_channel_msg_ops,
        sample_messages,
        sample_channel_metadata,
    ):
        """Test successful report generation."""
        # Arrange
        mock_channel_msg_ops.fetch_channel_messages.return_value = sample_messages
        mock_ai_response = """
h3. Executive Summary
Authentication service experienced certificate expiration causing login failures.

h3. People Involved
- U123: Identified the issue
- U456: Investigated the problem

h3. Incident Timeline
- 2024-01-01 00:00:00 UTC: Authentication errors reported
- 2024-01-01 00:01:00 UTC: Investigation started
- 2024-01-01 00:02:00 UTC: Root cause identified - expired certificate
"""
        mock_openai_handler.execute_prompt.return_value = mock_ai_response

        # Act
        result = await report_generator.generate_report(
            channel_id=sample_channel_metadata["channel_id"],
            channel_metadata=sample_channel_metadata,
        )

        # Assert
        assert result is not None
        assert "h2. Ketchup Automated Incident Report" in result
        assert "Adobe Campaign" in result
        assert "CPGNREQ-12345" in result
        assert "Executive Summary" in result
        assert "automatically generated by Ketchup" in result

        # Verify channel messages were fetched
        mock_channel_msg_ops.fetch_channel_messages.assert_called_once_with(
            channel_id="C123456", limit=500
        )

        # Verify AI was called with proper parameters
        mock_openai_handler.execute_prompt.assert_called_once()
        ai_call_args = mock_openai_handler.execute_prompt.call_args
        assert ai_call_args.kwargs["max_tokens"] == 2000
        assert ai_call_args.kwargs["reasoning_effort"] == "medium"

