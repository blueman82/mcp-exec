    def test_create_report_prompt_without_json_flag(
        self, report_generator, sample_channel_metadata, monkeypatch
    ):
        """
        Test that prompt does NOT contain 'json' keyword when feature flag is disabled.

        This test replicates the 400 error condition:
        Azure OpenAI API returns 400 if response_format is json_object but prompt
        doesn't contain the word "json" in any form.
        """
        # Arrange
        from packages.core.config.feature_flags import FeatureFlags

        # Mock feature flag to return False (JSON mode disabled)
        monkeypatch.setattr(FeatureFlags, "is_structured_json_output_enabled", lambda: False)
        formatted_messages = "Test messages"

        # Act
        result = report_generator._create_report_prompt(
            formatted_messages=formatted_messages,
            channel_metadata=sample_channel_metadata,
        )

        # Assert - prompt should NOT contain "json" when flag is disabled
        assert "json" not in result.lower()
        assert "JSON" not in result
        # Still has all the normal prompt content
        assert "JIRA Ticket: CPGNREQ-12345" in result
        assert "Test messages" in result

    def test_create_report_prompt_with_json_flag_enabled(
        self, report_generator, sample_channel_metadata, monkeypatch
    ):
        """
        Test that prompt CONTAINS 'json' keyword when feature flag is enabled.

        This test verifies the fix for the 400 error:
        When KETCHUP_STRUCTURED_JSON_OUTPUT=true, the prompt must include "json"
        to satisfy Azure OpenAI API requirements for response_format: json_object.
        """
        # Arrange
        from packages.core.config.feature_flags import FeatureFlags

        # Mock feature flag to return True (JSON mode enabled)
        monkeypatch.setattr(FeatureFlags, "is_structured_json_output_enabled", lambda: True)
        formatted_messages = "Test messages"

        # Act
        result = report_generator._create_report_prompt(
            formatted_messages=formatted_messages,
            channel_metadata=sample_channel_metadata,
        )

        # Assert - prompt MUST contain "json" when flag is enabled
        assert "json" in result.lower() or "JSON" in result
        # Verify specific JSON instruction text is present
        assert "Return your response as JSON" in result
        assert '{"response_text"' in result
        # Still has all the normal prompt content
        assert "JIRA Ticket: CPGNREQ-12345" in result
        assert "Test messages" in result
