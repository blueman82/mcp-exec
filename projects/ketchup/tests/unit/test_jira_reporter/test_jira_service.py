from jira_reporter.jira_service import JiraService


def create_mcp_response(data):
    """Helper function to create MCP protocol JSON-RPC response."""
    return {"result": data}


def create_mcp_error(code, message):
    """Helper function to create MCP protocol JSON-RPC error response."""
    return {"error": {"code": code, "message": message}}


