    @pytest.mark.asyncio
    async def test_post_comment_success(self, jira_service, sample_report):
        """Test successful comment posting to JIRA."""
        # Arrange
        with patch("httpx.AsyncClient") as mock_client_class:
            mock_client = AsyncMock()
            mock_client_class.return_value.__aenter__.return_value = mock_client

            # Mock successful ticket validation and comment posting (JSON-RPC responses)
            mock_client.post.side_effect = [
                # First call: ticket validation
                AsyncMock(
                    status_code=200,
                    json=lambda: {
                        "result": {
                            "content": [
                                {
                                    "text": '{"total": 1, "issues": [{"key": "'
                                    + self.TEST_TICKET_CPGNREQ
                                    + '"}]}'
                                }
                            ]
                        }
                    },
                ),
                # Second call: comment posting
                AsyncMock(
                    status_code=200,
                    json=lambda: {"result": {"content": [{"text": "Comment added successfully"}]}},
                ),
            ]

            # Act
            result = await jira_service.post_comment_to_ticket(
                jira_ticket_id=self.TEST_TICKET_CPGNREQ, comment_text=sample_report
            )

            # Assert
            assert result is True

            # Verify both API calls were made
            assert mock_client.post.call_count == 2

            # Verify first call (ticket validation)
            first_call = mock_client.post.call_args_list[0]
            assert "/message" in first_call.args[0]
            validation_payload = first_call.kwargs["json"]
            assert validation_payload["method"] == "tools/call"
            assert validation_payload["params"]["name"] == "search_jira_issues"

            # Verify second call (comment posting)
            second_call = mock_client.post.call_args_list[1]
            assert "/message" in second_call.args[0]
            comment_payload = second_call.kwargs["json"]
            assert comment_payload["method"] == "tools/call"
            assert comment_payload["params"]["name"] == "add_jira_comment"
            assert (
                comment_payload["params"]["arguments"]["issueIdOrKey"] == self.TEST_TICKET_CPGNREQ
            )
            assert comment_payload["params"]["arguments"]["comment"]["body"] == sample_report

